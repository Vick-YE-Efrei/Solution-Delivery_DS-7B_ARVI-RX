function tBackend(str) {
  if (locale.value === 'fr' || !str) return str
  if (_backendEn[str]) return _backendEn[str]
  if (str.startsWith("terme détecté : '")) {
    return `term detected: '${str.slice(17, -1)}'`
  }
  if (str.startsWith("ambigu : '")) {
    return `ambiguous: ${str.slice(8)}`
  }
  if (str.startsWith("langage d'incertitude : '")) {
    return `uncertainty language: '${str.slice(25, -1)}'`
  }
  if (str.startsWith("Description générée par le modèle : ")) {
    return `Model-generated description: ${str.slice(36)}`
  }
  if (str.startsWith("Classe déduite par mots-clés depuis la description libre générée par ")) {
    const rest = str.slice(69)
    const dot = rest.indexOf('. ')
    const modelName = dot >= 0 ? rest.slice(0, dot) : rest
    return `Class inferred by keywords from the free description generated by ${modelName}. Experimental result, non-clinical.`
  }
  return str
}

