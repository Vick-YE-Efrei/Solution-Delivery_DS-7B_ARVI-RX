function t(key) {
  const keys = key.split('.')
  let val = translations[locale.value]
  for (const k of keys) {
    if (val == null) return key
    val = val[k]
  }
  return val ?? key
}

function toggleLocale() {
  locale.value = locale.value === 'fr' ? 'en' : 'fr'
  localStorage.setItem('ravi_locale', locale.value)
}

